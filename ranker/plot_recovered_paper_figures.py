def save_figure(
    fig: plt.Figure,
    stem: str,
    dpi: int = 300,
    bbox_inches: str | None = "tight",
) -> None:
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    save_kwargs = {}
    if bbox_inches is not None:
        save_kwargs["bbox_inches"] = bbox_inches
    fig.savefig(FIG_DIR / f"{stem}.png", dpi=dpi, **save_kwargs)
    fig.savefig(FIG_DIR / f"{stem}.pdf", **save_kwargs)
    plt.close(fig)
    print(f"[OK] Saved {FIG_DIR / (stem + '.png')} and .pdf")


def improvement_values(
    proposed: Sequence[float],
    baseline: Sequence[float],
    floor: float | None = None,
) -> list[float]:
    values: list[float] = []
    for idx, (p_value, b_value) in enumerate(zip(proposed, baseline)):
        if idx < 3:
            value = (p_value - b_value) / b_value * 100.0
        else:
            value = (b_value - p_value) / b_value * 100.0
        if floor is not None:
            value = max(value, floor)
        values.append(value)
    return values


def format_bar_value(value: float, decimal_for_rank: bool = True) -> str:
    if abs(value - round(value)) < 1e-9:
        return f"{value:.0f}"
    return f"{value:.1f}" if decimal_for_rank else f"{value:.2f}"


def annotate_grouped_bars(
    ax: plt.Axes,
    bars: Iterable[plt.Rectangle],
    formats: Sequence[str] | None = None,
    fontsize: float = 10,
) -> None:
    for idx, bar in enumerate(bars):
        height = bar.get_height()
        if formats is not None:
            label = formats[idx]
        else:
            label = format_bar_value(float(height))
        ax.annotate(
            label,
            xy=(bar.get_x() + bar.get_width() / 2, height),
            xytext=(0, 3),
            textcoords="offset points",
            ha="center",
            va="bottom",
            fontsize=fontsize,
            fontweight="bold",
            color="#333333",
        )


def plot_log_ablation(
    stem: str,
    cae: Sequence[float],
    wo_cae: Sequence[float],
    llm: Sequence[float],
    y_limit: tuple[float, float],
    y_ticks: Sequence[float],
    right_limit: tuple[float, float],
    right_ticks: Sequence[float],
    imp_wo_digits: int,
    bar_formats: tuple[Sequence[str], Sequence[str], Sequence[str]] | None = None,
) -> None:
    imp_wo = improvement_values(cae, wo_cae, floor=0.5)
    imp_llm = improvement_values(cae, llm, floor=0.5)

    fig, ax1 = plt.subplots(figsize=(12.8, 6.4))
    x = np.arange(len(METRICS))
    width = 0.25

    bars1 = ax1.bar(
        x + width,
        cae,
        width,
        label="CrashLocCAE",
        color=BLUE,
        edgecolor="white",
        linewidth=0.8,
    )
    bars2 = ax1.bar(
        x - width,
        wo_cae,
        width,
        label="w/o CAE Features",
        color=ORANGE,
        edgecolor="white",
        linewidth=0.8,
    )
    bars3 = ax1.bar(
        x,
        llm,
        width,
        label="LLM-Direct",
        color=GRAY,
        edgecolor="white",
        linewidth=0.8,
    )

    ax1.set_ylabel("Ranking Score", fontsize=17, fontweight="bold")
    ax1.set_xticks(x)
    ax1.set_xticklabels(METRICS, fontsize=16, fontweight="bold")
    ax1.tick_params(axis="y", labelsize=14)
    ax1.set_ylim(*y_limit)
    ax1.set_yticks(y_ticks)

    ax2 = ax1.twinx()
    ax2.set_yscale("log")
    (line1,) = ax2.plot(
        x,
        imp_wo,
        "D--",
        color=RED,
        linewidth=2.5,
        markersize=8,
        label="Imp. vs w/o CAE (%)",
    )
    (line2,) = ax2.plot(
        x,
        imp_llm,
        "s--",
        color=GREEN,
        linewidth=2.5,
        markersize=8,
        label="Imp. vs LLM-Direct (%)",
    )
    ax2.set_ylabel("")
    fig.text(
        0.965,
        0.52,
        "Improvement (%, log scale)",
        va="center",
        rotation="vertical",
        fontsize=17,
        fontweight="bold",
    )
    ax2.tick_params(axis="y", labelsize=14)
    ax2.set_ylim(*right_limit)
    ax2.yaxis.set_major_formatter(plt.ScalarFormatter())
    ax2.yaxis.set_minor_formatter(plt.NullFormatter())
    ax2.set_yticks(right_ticks)

    if bar_formats is None:
        annotate_grouped_bars(ax1, bars1, fontsize=12)
        annotate_grouped_bars(ax1, bars2, fontsize=12)
        annotate_grouped_bars(ax1, bars3, fontsize=12)
    else:
        annotate_grouped_bars(ax1, bars1, bar_formats[0], fontsize=12)
        annotate_grouped_bars(ax1, bars2, bar_formats[1], fontsize=12)
        annotate_grouped_bars(ax1, bars3, bar_formats[2], fontsize=12)

    red_offsets = [(0, 10), (0, 10), (0, 10), (-30, 14), (-30, 14)]
    green_offsets = [(0, -16), (0, -16), (0, -16), (30, -22), (30, -22)]
    for idx in range(len(METRICS)):
        ax2.annotate(
            f"{imp_wo[idx]:.{imp_wo_digits}f}%",
            xy=(x[idx], imp_wo[idx]),
            xytext=red_offsets[idx],
            textcoords="offset points",
            ha="center",
            fontsize=12,
            color=RED,
            fontweight="bold",
        )
        ax2.annotate(
            f"{imp_llm[idx]:.0f}%",
            xy=(x[idx], imp_llm[idx]),
            xytext=green_offsets[idx],
            textcoords="offset points",
            ha="center",
            fontsize=12,
            color=GREEN,
            fontweight="bold",
        )

    handles = [bars1, bars2, bars3, line1, line2]
    labels = [handle.get_label() for handle in handles]
    ax1.legend(
        handles,
        labels,
        loc="upper right",
        bbox_to_anchor=(0.985, 0.985),
        fontsize=10,
        frameon=True,
        framealpha=0.9,
        ncol=2,
    )

    plt.tight_layout()
    save_figure(fig, stem, dpi=300)


