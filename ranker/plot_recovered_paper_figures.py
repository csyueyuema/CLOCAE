def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "figures",
        nargs="*",
        help="Figure stem(s) to rebuild. Defaults to all recovered figures.",
    )
    parser.add_argument("--list", action="store_true", help="List available recovered figure stems.")
    args = parser.parse_args()

    if args.list:
        for name in sorted(FIGURES):
            print(name)
        return

    selected = args.figures or ["all"]
    unknown = [name for name in selected if name != "all" and name not in FIGURES]
    if unknown:
        parser.error(f"unknown figure stem(s): {', '.join(unknown)}")

    if "all" in selected:
        selected = list(FIGURES)

    for name in selected:
        FIGURES[name]()


if __name__ == "__main__":
    main()
